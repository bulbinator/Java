public class multiArray {

    public static void main(String[] args) {

        /* 1-D array*/
        int[] values = {3, 5, 2343};
        System.out.println(values[2]);

        /*2-D array. Holds arrays*/
        int[][] grid = {
                {3, 5, 2345},
                {2, 4},
                {5, 78}
        };

        System.out.println(grid[0][2]);
    }
}
